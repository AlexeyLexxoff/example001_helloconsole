{
  sqrMatrix[i, j] = temp;
  temp++;
  if (i <= j + 1 && i + j < sqrMatrix.GetLength(1) - 1)
    j++;
  else if (i < j && i + j >= sqrMatrix.GetLength(0) - 1)
    i++;
  else if (i >= j && i + j > sqrMatrix.GetLength(1) - 1)
    j--;
  else
    i--;
}

