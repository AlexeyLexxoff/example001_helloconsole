void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++) // - внешний 3
    {
        for (int j = 0; j < image.GetLength(1); j++) // - внутренний 4
        {
            // System.Console.Write($"{image[i, j]} ");
            if (image[i, j] == 0) System.Console.Write($" ");
            else System.Console.Write("1");
        }
        System.Console.WriteLine();
    }
}

void FillImage(int row, int col)
{    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;

        FillImage(row - 1, col);
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);
    }
}


